num1=int(input("Enter first number : "))
num2=int(input("Enter second number : "))
print(num1,"+",num2,"=",format(num1+num2,',d'))
print(num1,"-",num2,"=",format(num1-num2,',d'))
print(num1,"*",num2,"=",format(num1*num2,',d'))
print(num1,"/",num2,"=",format(num1/num2,',.2f'))
print(num1,"//",num2,"=",format(num1//num2,',d'))
print(num1,"%",num2,"=",format(num1%num2,',d'))
print(num1,"**",num2,"=",format(num1**num2,',d'))
